def pick_story(tag: str | None = None,
               *, name: str, rod: str, bait: str) -> tuple[str, str, str, str]:
    """隨機（或依 tag）抽三段組合。回 (tag_used, s1, s2, s3)。
    placeholder {name}/{rod}/{bait} 已套換。"""
    tag = tag or pick_tag()
    s1 = random.choice(STAGE1.get(tag) or STAGE1[TAG_SUNNY_CAT])
    s2 = random.choice(STAGE2.get(tag) or STAGE2[TAG_SUNNY_CAT])
    s3 = random.choice(STAGE3.get(tag) or STAGE3[TAG_SUNNY_CAT])
    ctx = {'name': name, 'rod': rod, 'bait': bait}
    return tag, s1.format(**ctx), s2.format(**ctx), s3.format(**ctx)

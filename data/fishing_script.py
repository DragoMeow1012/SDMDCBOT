    s2 = random.choice(STAGE2.get(tag) or STAGE2[TAG_SUNNY_CAT])
    s3 = random.choice(STAGE3.get(tag) or STAGE3[TAG_SUNNY_CAT])
    ctx = {'name': name, 'rod': rod, 'bait': bait}
    return tag, s1.format(**ctx), s2.format(**ctx), s3.format(**ctx)
